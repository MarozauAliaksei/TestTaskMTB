def voice_for(speaker: str) -> str:
    return VOICE_OPERATOR if speaker == "Оператор" else VOICE_CLIENT


def synthesize_line(text: str, voice: str, out_path: Path) -> None:
    subprocess.run(
        ["edge-tts", "--voice", voice, "--text", text, "--write-media", str(out_path)],
        check=True,
    )


def build_dialogue(name: str, lines: list) -> float:
    print(f"=== {name} ===")
    line_audios = []
    for i, (speaker, text) in enumerate(lines):
        line_path = TMP_DIR / f"{name}_{i:02d}.mp3"
        synthesize_line(text, voice_for(speaker), line_path)
        line_audios.append((speaker, AudioSegment.from_mp3(line_path)))

    # Длинная пауза (>= порога диаризации) — только на реальной смене говорящего.
    # Между репликами ОДНОГО говорящего пауза короткая (ниже порога диаризации),
    # иначе она неотличима от паузы на смене говорящего, и эвристика по паузам
    # в diarizer.py не сможет их различить (это реально ломало диаризацию —
    # см. историю правок в README).
    turn_change_silence = AudioSegment.silent(duration=1200)
    same_speaker_silence = AudioSegment.silent(duration=250)

    combined = AudioSegment.empty()
    for i, (speaker, seg) in enumerate(line_audios):
        combined += seg
        if i < len(line_audios) - 1:
            next_speaker = line_audios[i + 1][0]
            combined += turn_change_silence if next_speaker != speaker else same_speaker_silence
        else:
            combined += turn_change_silence

    out_path = OUT_DIR / f"{name}.mp3"
    combined.export(out_path, format="mp3")
    duration_sec = len(combined) / 1000
    print(f"  -> {out_path} ({duration_sec:.1f} сек)")

    ref_path = OUT_DIR / f"{name}.txt"
    ref_path.write_text(
        "\n".join(f"{speaker}: {text}" for speaker, text in lines),
        encoding="utf-8",
    )
    print(f"  -> {ref_path}")
    return duration_sec


def main():
    total_sec = 0.0
    for name, lines in DIALOGUES.items():
        total_sec += build_dialogue(name, lines)

    print(f"\nОбщая длительность всех файлов: {total_sec:.1f} сек ({total_sec / 60:.1f} мин)")
    if total_sec < 300:
        print("⚠️  Меньше 5 минут по ТЗ — добавь ещё реплик в DIALOGUES и перезапусти.")

    first = OUT_DIR / "call_1_kredity.mp3"
    eightk = OUT_DIR / "call_1_kredity_8khz.wav"
    subprocess.run(
        ["ffmpeg", "-y", "-i", str(first), "-ar", "8000", "-ac", "1", str(eightk)],
        check=True,
    )
    print(f"8kHz версия (телефонное качество): {eightk}")


if __name__ == "__main__":
    main()
